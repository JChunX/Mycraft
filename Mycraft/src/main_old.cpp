int main()
{
	glfwInit();
	glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
	glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 3);
	glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);
	GLFWwindow* window = glfwCreateWindow(SCR_WIDTH, SCR_HEIGHT, "GLFW Window", NULL, NULL);

	if (window == NULL)
	{
		std::cout << "Failed to create GLFW window" << std::endl;
		glfwTerminate();
		return -1;
	}

	glfwMakeContextCurrent(window);
	glfwSetFramebufferSizeCallback(window, framebuffer_size_callback);

	gladLoadGL();
	glViewport(0, 0, SCR_WIDTH, SCR_HEIGHT);
	glEnable(GL_DEPTH_TEST);  

	Shader default_shader("shaders/block.vert", "shaders/block.frag");
	BlockRenderer block_renderer("textures/default_pack.png", vertices, sizeof(vertices) / sizeof(float));
	default_shader.Activate();
	Camera camera(SCR_WIDTH, 
				SCR_HEIGHT, 
				window, 70.0f, 0.1f, 50.0f, glm::vec3(0.0f, 0.0f, 1.0f), glm::vec3(0.0f, 0.0f, 0.0f));
	camera.SetProjectionUniform(default_shader, "projection");

	InputBroadcaster::AddListener(camera);

	float last_frame = glfwGetTime();

	// create vector of blocks
	std::vector<Block> blocks;
	for (int i = 0; i < 200; i++)
	{
		for (int j = 0; j < 100; j++)
		{
			Block block(GRASS, glm::vec3(2* i, 0, j));
			blocks.push_back(block);
		}
	}                      

	while (!glfwWindowShouldClose(window))
	{
		float current_frame = glfwGetTime();
		float delta_time = current_frame - last_frame;
		last_frame = current_frame;
		std::cout << "FPS: " << 1.0f / delta_time << std::endl;

		glClearColor(0.07f, 0.13f, 0.17f, 1.0f);
		glClear(GL_COLOR_BUFFER_BIT);
		glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

		default_shader.Activate();
		InputBroadcaster::ReadInputs(window);
		camera.SetViewUniform(default_shader, "view");

		for (auto block : blocks) {
			block_renderer.Render(default_shader, block);
		}

		check_error(1);

		glfwSwapBuffers(window);
		glfwPollEvents();
	}

	default_shader.Delete();
	glfwDestroyWindow(window);
	glfwTerminate();
	return 0;
}



void check_error(int checkpoint)
{
	GLenum error = glGetError();
	if (error != GL_NO_ERROR)
	{
		std::cout << "OpenGL Error: " << error << " at checkpoint: " << checkpoint << std::endl;
	}
}