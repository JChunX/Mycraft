Skybox::~Skybox()
{
    Delete();
}

void Skybox::Buffer()
{
    m_vao.Bind();
    m_vbo.Bind();
    float* vertices = &m_vertices[0];
    m_vbo.Buffer(vertices, m_vertices.size() * sizeof(float));
    m_vao.LinkAttrib(m_vbo, 0, 3, GL_FLOAT, 3 * sizeof(float), (void*)0);
    m_vao.Unbind();
    m_vbo.Unbind();
}

void Skybox::Render()
{
    m_vao.Bind();
    m_texture_manager.BindTexture(*(m_texture_manager.m_skybox_texture));
    glDrawArrays(GL_TRIANGLES, 0, 36);
    m_vao.Unbind();
}